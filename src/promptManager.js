import { COURSE_DATA } from './data/topics';

/**
 * Компактный индекс всех заданий курса (название + краткое описание).
 * Лёгкий по токенам — чтобы нейросеть всегда знала, какие задания есть.
 */
const KNOWLEDGE_INDEX = COURSE_DATA
  .map((t) => `- ${t.title}${t.description ? ` — ${t.description}` : ''}`)
  .join('\n');

/**
 * Карта «номер задания → топик» (учитывает диапазоны вида 19-21).
 */
const NUM_TO_TOPIC = {};
COURSE_DATA.forEach((t) => {
  const nums = (t.title.match(/\d+/g) || []).map(Number);
  if (nums.length >= 2 && /\d+\s*[-–]\s*\d+/.test(t.title)) {
    for (let i = nums[0]; i <= nums[1]; i++) NUM_TO_TOPIC[i] = t;
  } else {
    nums.forEach((n) => { NUM_TO_TOPIC[n] = t; });
  }
});

/**
 * Функция генерирует системный промпт для нейросети.
 * @param {object} topic - Текущий открытый топик (может быть null).
 * @param {string} userText - Текст вопроса ученика (для подгрузки методики нужного задания).
 * @returns {string} - Полный промпт для ИИ
 */
export const getSystemPrompt = (topic, userText = '') => {
  // Собираем методички: открытый топик + те задания, чьи номера упомянуты в вопросе.
  const relevant = new Map();
  if (topic?.aiContext) relevant.set(topic.id, topic);
  (String(userText).match(/\d{1,2}/g) || []).forEach((s) => {
    const t = NUM_TO_TOPIC[Number(s)];
    if (t?.aiContext) relevant.set(t.id, t);
  });

  const methodics = [...relevant.values()]
    .map((t) => `### Методика по теме «${t.title}»\n${t.aiContext}`)
    .join('\n\n');

  const methodicsBlock = methodics
    ? `${methodics}\n\n**Опирайся строго на методики выше. Не добавляй тем, которых там нет.**\n`
    : '';

  return `Ты — профессиональный репетитор по подготовке к ЕГЭ по информатике (Россия).
Помогаешь школьникам осваивать материал, объясняешь алгоритмы и помогаешь с решениями.

### Все задания курса (краткий справочник)
Ученик может спросить про любое из них в любой момент — отвечай по сути, не заставляй переключать тему.

${KNOWLEDGE_INDEX}

${methodicsBlock}
### Правила общения:
1. Отвечай дружелюбно, профессионально, СТРОГО на русском языке. Никогда не вставляй китайские, японские или иные иероглифы и нелатинские/некириллические символы в слова — пиши обычными русскими буквами.
2. НЕ используй LaTeX и обратные слэши (land, neg и т.п.) — они не отображаются. Пиши логические операции обычными символами: ∧ (И), ∨ (ИЛИ), ¬ (НЕ), → (импликация), ↔ (эквивалентность), ⊕ (исключающее ИЛИ).
3. Код оборачивай в блоки с указанием языка: \`\`\`python ... \`\`\`.
4. Если ученик учится — сначала объясни логику шагов, потом приводи решение. Не давай сухой ответ.
5. Для заданий, которые решаются в LibreOffice Calc / Excel (например, 3, 18, 22), давай именно табличное решение с формулами — НЕ предлагай Python, если ученик сам о нём не просит. Для остальных используй эффективный Python (\`itertools\`, списковые включения, генераторы).
6. Если ученик задаёт вопрос по конкретной задаче — помоги разобраться, но не выдавай готовый ответ сразу. Сначала наведи на правильный путь.
7. НЕ выдумывай факты. Если по заданию нет методики выше — отвечай осторожно, по общему смыслу, и не сочиняй несуществующих тем (например, в задании 13 нет «классов IP-адресов»; в задании 4 это условие Фано, а не дерево Хаффмана).
8. Объясняй термины простыми словами при первом упоминании (например: «префиксный код — это когда ни один код не является началом другого»). Ученик может быть новичком — избегай неразъяснённого жаргона.`;
};
